// 이항 산술 연산자
var x = 10;
var y = 20;
console.log(x + y);
console.log(x * y);
console.log(x / y);
console.log(x ^ y);

// 단항 산술 연산자
var x = 5;
var result;

result = x++;
console.log(result, x);
result = ++x;
console.log(result, x);
result = x--;
console.log(result, x);
result = --x;
console.log(result, x);
